  <section class="six-section">
  <div class="container">
  <h2 class="text-center fourth-section__heading mb-4 p-2">STRATEGIES</h2>

        <div class="row">
            <div class="col-md-4 six-section--hover-add-border-effect">
                <p class="text-center">
                    <i class="fa fa-group fa-5x" style="color: #e0524e;"></i>
                </p>
                <h4 class="text-center"><strong>Youth Groups' Formation</strong></h4>


                <p class="text-center">
                The strategy is being explored to ensure sustainability 
                of the various programmes mounted by the Centre. 
                The strategy emphasizes the youths' organizational 
                building and development by mobilizing and facilitating 
                the youth that want to participate in the Centre's programmes 
                to form themselves into groups in line with the Centre's guidelines.
              </p>
            </div>
            <div class="col-md-4 six-section--hover-add-border-effect">
                <p class="text-center">
                    <i class="fa fa-handshake-o fa-5x" style="color: #e0524e;"></i>
                </p>
                <h4 class="text-center"><strong>Institutional Affiliation and Linkages</strong></h4>
                <p class="text-center">
                This strategy is being explored to provide space for any 
                youth who may be interested in the Centre's apprenticeship supports 
                and scholarship awards. The Centre will be willing to sign a memorandum 
                of understanding with any credible vocational centre that will be 
                willing to admit any of its participants as an apprentice. 
                It will also ensure a very strong linkage with any educational 
                institution of its scholarship awardees to facilitate proper monitoring.
              </p>
            </div>
            <div class="col-md-4 six-section--hover-add-border-effect">
                <p class="text-center">
                    <i class="fa fa-line-chart fa-5x" style="color: #e0524e;"></i>
                </p>
                <h4 class="text-center"><strong>Promoting Savings and Revolving Credits</strong></h4>
                <p class="text-center">
                This strategy is to build the youth financial capacity through 
                culture of saving in order to facilitate a financial pool for 
                credit facility among the youth group.
              </p>
            </div>
            <div class="col-md-4 six-section--hover-add-border-effect">
                <p class="text-center">
                    <i class="fa fa-graduation-cap fa-5x" style="color: #e0524e;"></i>
                </p>
                <h4 class="text-center"><strong>Training and Education for Empowerment</strong></h4>
                <p class="text-center">
                This strategy is a major tool for ensuring attitudinal and 
                behaviour change in the process of capacity building and 
                youth empowerment. This is being explored through the use of 
                multi media and communication resources (printed and electronics media), 
                peer education and experiential methodologies. The Centre uses its Partners, 
                Fellows and Volunteers as Programme Officers in implementing most of its programmes, 
                while the resource persons are employed for training on a part time basis.                </p>
            </div>
          
        </div>

    </div>
  </section>

