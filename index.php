                as his country's action programme during his participation at the German Government/FAO's
                sponsored 21st International Rural Youth Leaders Training Workshop held in Bavarian Farmers'
                Union Training Center, Herrsching, Germany between June and July 2002.  

                <section class="second-section__secondary-text">
                <hr>
                    ACRYE was officially inaugurated in Nigeria on the 11th of November, 2004 at the Amphi 
                    Theatre, Obafemi Awolowo University (OAU), Ile-Ife, Osun State, Nigeria. Also, its maiden
                    series of Youth Empowerment Publication entitled: Ethical Revolution among Nigerian Youth
                    in Schools and Colleges was launched during the inauguration. The ceremony, which was widely
                    publicized by various media in Osun State, most especially, National Television Authority (NTA),
                    Ile-Ife, Osun State Broadcasting Corporation (OSBC), Osogbo and Tribune Newspaper, was well attended
                    by over 300 people of various classes including youth and adult. It was a very huge success!
