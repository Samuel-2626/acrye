  <section class="third-section">
    <div class="container">
      <div class="row">
        <div class="col-md-6">
          <h2 class="text-center third-section__heading p-2">OUR MISSION</h2>
        </div>
        <div class="col-md-6">
          <br>
          <p>
            ACRYE is a holistic develoment initiative for creating opportunities for young men and
            women residing in rural communities of Nigeria by empowering them for a decent livelihood,
            through a group-oriented capacity building, skill development and entreprenuerial 
            endeavours.
          </p>
        </div>
      </div>
    </div>
  </section>

  <section class="fourth-section">
  <div class="container">
  <h2 class="text-center fourth-section__heading mb-5 p-2">AIMS & OBJECTIVES</h2>
    <div class="row">
      <div class="col-md-6">
      <ul>
          <li>To develop and promote ideas and/or organize programmes and/or engage in activities
            that will enhance adequate development of rural youth either solely or in collaboration 
            with government agency, non governmental organizations, community based organizations, 
            international and/or foreign agencies.
          </li>
          <li>
            To sensitize and organise rural youth into groups for development and empowerment programmes.
          </li>
          <li>
            To develop and promote talents among the rural youth.
          </li>
          <li>
            To organize local, national, international learned conferences, seminars, 
            workshops and awards focusing on the youth capacity building, and empowerment 
            initiatives for full integration into the main stream of agricultural, rural 
            and national economic development in Nigeria.
          </li>
        </ul>
      </div>
      <div class="col-md-6 fourth-section--hide-on-small-device">
        <ul>
          <li>
            To organise and sponsor local, national and international competitions, 
            shows, exhibitions, holidays and exchange programmes for rural youth.
          </li>
          <li>
            To organise training for rural youth on: exploration of rural resources, 
            improved farm practices, poverty alleviation techniques, life enhancement and 
            sex education, HIV/AIDS prevention and income generating/ entrepreneurial activities, 
            information and communication management technologies etc.
          </li>
          <li>
            To participate in and contribute to rural youth advancement activities 
            at the local, national and international levels.
          </li>
          <li>
            To conduct periodic documentation and keeping socio-economic data of 
            rural youth situation in Nigeria.
          </li>
          <li>
            To produce, source and circulate printed and non-printed materials 
            related to the youth for the purpose of education and training.
          </li>
        </ul>
      </div>
    </div>
  </div>
  </section>

